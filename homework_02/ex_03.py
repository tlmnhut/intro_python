num_list = [3, 2, 1, 4, 5, 6, 8, 7, 9, 0]
num_list.sort()
print(num_list)

# largest element
print(num_list[-1])
# or without .sort()
print(max(num_list))

# smallest element
print(num_list[0])
# or without .sort()
print(min(num_list))

# second largest element
print(num_list[-2])

# third smallest element
print(num_list[2])
